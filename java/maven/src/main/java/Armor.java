public class Armor {
    private String name;
    private int valueOfDefence;

    public Armor(String name, int valueOfDefence) {
        this.name = name;
        this.valueOfDefence = valueOfDefence;
    }

    public String getName() {
        return name;
    }

    public int getValueOfDefence() {
        return valueOfDefence;
    }
}
